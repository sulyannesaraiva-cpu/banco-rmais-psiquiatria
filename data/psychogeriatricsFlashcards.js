function getFlashcardsByModule(module) {
  return psychogeriatricsFlashcards.filter((card) => normalizeFlashcardValue(card.module) === normalizeFlashcardValue(module));
}

function getHighPriorityFlashcards(minPriority = 4) {
  return psychogeriatricsFlashcards.filter((card) => card.priority >= minPriority);
}

function getFlashcardsByPriority(priority, mode = "min") {
  return psychogeriatricsFlashcards.filter((card) => (mode === "exact" ? card.priority === priority : card.priority >= priority));
}

function getFlashcardsByTag(tag) {
  const needle = normalizeFlashcardValue(tag);
  return psychogeriatricsFlashcards.filter((card) => card.tags.some((item) => normalizeFlashcardValue(item) === needle));
}

function getFlashcardsForUSP(minFrequency = 4) {
  return psychogeriatricsFlashcards.filter((card) => (card.examFrequency?.USP || 0) >= minFrequency);
}

function getFlashcardsByTopic(topic) {
  return psychogeriatricsFlashcards.filter((card) => normalizeFlashcardValue(card.topic) === normalizeFlashcardValue(topic));
}

function getFlashcardsByType(type) {
  return psychogeriatricsFlashcards.filter((card) => normalizeFlashcardValue(card.type) === normalizeFlashcardValue(type));
}

function getFlashcardsByExamFrequency(board, minFrequency = 4) {
  return psychogeriatricsFlashcards.filter((card) => (card.examFrequency?.[board] || 0) >= minFrequency);
}

function normalizeFlashcardValue(value) {
  return String(value || "")
    .toLowerCase()
    .normalize("NFD")
    .replace(/\p{Diacritic}/gu, "");
}

function flashcardPriorityBuckets(card) {
  const buckets = [];
  if (card.priority >= 4 || (card.examFrequency?.USP || 0) >= 4) buckets.push("high-frequency");
  if (["Diagnostico", "Tratamento", "Medicamentos"].includes(card.topic)) buckets.push("weak-performance");
  if (["Diagnostico diferencial", "Farmacologico", "Benzodiazepinicos", "Contraindicacoes relativas"].includes(card.type)) buckets.push("repeated-error");
  if (card.priority >= 5 || card.tags.some((tag) => ["antipsicotico", "benzodiazepinico", "haloperidol", "Lewy", "abstinencia"].includes(tag))) buckets.push("dangerous");
  return [...new Set(buckets)];
}

function toBancoRmaisFlashcard(card) {
  return {
    id: card.id,
    front: card.front,
    back: card.back,
    tags: card.tags,
    priorities: flashcardPriorityBuckets(card),
    priority: card.priority,
    importance: card.importance || card.priority || 5,
    specialty: card.specialty,
    module: card.module,
    topic: card.topic,
    type: card.type,
    examFrequency: card.examFrequency,
    whyExamAsks: card.whyExamAsks,
    trap: card.trap,
    memoryHook: card.memoryHook,
    related: card.related,
    sources: card.sources,
    lastReviewedAt: card.lastReviewedAt,
    nextReviewAt: card.nextReviewAt,
    easeFactor: card.easeFactor,
    intervalDays: card.intervalDays,
    repetitions: card.repetitions,
    lapses: card.lapses,
  };
}

